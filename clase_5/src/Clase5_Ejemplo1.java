
public class Clase5_Ejemplo1 {

	public static void main(String[] args) {
		
		final int MAX = 4;
		int numero = 1;
		
		while (numero <= MAX){
			System.out.println("El valor es: " + numero);
			numero++;
			System.out.println("Nuevo valor es: " + numero);
			
			//al cambiar de valor el numero significa que el valor de la expresion va a cambiar
		}

	}

}
