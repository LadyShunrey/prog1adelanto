public class Clase3_ejemplo3 {
	public static void main(String[] args) {
		//operadores incrementales
		int a, b;
		a = 5;
		a++;
		System.out.println("a = " + a);
		a = 5;
		++a;
		System.out.println("a = " + a);
		a = 5;
		b = a++;
		System.out.println("a = " + a + ", b = " + b);
		a = 5;
		b = ++a;
		System.out.println("a = " + a + ", b = " + b);
	}
}
